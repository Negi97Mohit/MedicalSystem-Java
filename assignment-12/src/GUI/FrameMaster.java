    private void jButton11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton11MouseClicked
        // TODO add your handling code here:

        JFileChooser filechoose=new JFileChooser();
        filechoose.addChoosableFileFilter(new FileNameExtensionFilter("Images", "jpg", "png", "gif", "bmp"));

        if (filechoose.showOpenDialog(null)==JFileChooser.APPROVE_OPTION){
            java.io.File file_name=filechoose.getSelectedFile();
            txt_img_filename.setText(filechoose.getSelectedFile().getPath());
            img_file_path=filechoose.getSelectedFile().getPath();
        }

    }//GEN-LAST:event_jButton11MouseClicked

